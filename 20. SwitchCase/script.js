let nilai = 70;

switch (true) {
    case nilai >= 75:
        console.log("Nilai anda Mantap");
        break;
    case nilai >= 60:
        console.log("Nilai anda cukup");
        break;
    case nilai >= 50:
        console.log("Nilai anda kurang");
        break;
    default:
        console.log("Anda salah jurusan");
        break;
}